      <div class="small-9 columns">
        <h2>Jerry Kang</h2>
        <p>Jerry Kang is the vice chancellor for Inclusion, Diversity and Equity, a position that was created last year, after a report conducted by a former California Supreme Court Justice found UCLA insufficiently addressed claims of racial discrimination. </p>
        <p>Kang&#39;s office aims to create the procedures and capacity to conduct independent investigations of alleged discrimination, harassment and sexual violence. Before he was selected for the position, Kang said he plans to use empirical evidence to cite cases of discrimination by comparing salaries, employee skill level and race, for example.</p>
        <p>Professor Kang graduated magna cum laude from Harvard College and Harvard Law School, where he was a supervising editor of the Harvard Law Review. Before coming to UCLA in 1995, Kang was a clerk for the Ninth Circuit Court of Appeals. Kang is also an Asian American studies and law professor at UCLA. </p>
      </div>
    </div>

    <div class="person" id="john-mazziotta">
      <div class="small-3 columns">
        <img src="http://dailybruin.com/images/2015/09/4.jpg"/>
      </div>
      <div class="small-9 columns">
        <h2>John Mazziotta</h2>
        <p>John Mazziotta is the vice chancellor of UCLA Health Sciences and CEO of UCLA Health. This year, Mazziotta&#39;s office aims to integrate education, research and patient care within the David Geffen School of Medicine, UCLA Health and other health sciences schools. To maintain UCLA hospital&#39;s high rankings, Mazziotta wants to increase philanthropic support and move forward in scientific research.</p>
        <p>Mazziotta was previously the chair of the Department of Neurology. He established the Brain Mapping Center at UCLA, and was the principal investigator of the International Consortium for Brain Mapping, a group that aims to create the first atlas of the human brain by collecting data from thousands of human subjects. From 2012 to 2015, he was associate vice chancellor for Health Sciences and executive vice dean of the David Geffen School of Medicine.</p>
      </div>
    </div>

    <div class="person" id="james-economou">
