        <h2>Jerry Kang</h2>
        <p>Jerry Kang is the vice chancellor for equity, diversity and inclusion, a position that was created last year after a report conducted by former California Supreme Court Justice Carlos Moreno found UCLA insufficiently addressed claims of racial discrimination. </p>
<p>Kang&#39;s office aims to create the procedures and capacity to conduct independent investigations of alleged discrimination, harassment and sexual violence. Before he was selected for the position, Kang said he planned to use empirical evidence to cite cases of discrimination by comparing salaries to employee skill level, for example.</p>
<p>Kang graduated magna cum laude from Harvard University and Harvard Law School, where he served as supervising editor of the Harvard Law Review. Before coming to UCLA in 1995, Kang was a clerk for the 9th U.S. Circuit Court of Appeals. Kang is also an Asian American studies and law professor at UCLA.</p>
      </div>
