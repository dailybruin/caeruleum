      <div class="small-9 columns">
        <h2>James S. Economou</h2>
        <p>James S. Economou is the vice chancellor for Research. Economou&#39;s office aims to solve UCLA Grand Challenges, a term borrowed from the White House that refers to diseases, illiteracy, inequality and inaccessibility to clean water. Economou also wants to increase the entrepreneurial culture at UCLA because he believes these breakthroughs will not influence the rest of society unless they are commercialized. </p>
        <p>Economou is also the senior surgical oncologist at the UCLA School of Medicine, a resident of the American Surgical Association and founding director of the UCLA Human Gene Medicine Program.</p>
      </div>
    </div>

