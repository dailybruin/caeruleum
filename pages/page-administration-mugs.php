        <h2>Gene Block</h2>
        <p>Gene Block is the chancellor of UCLA. In his capacity as chief executive officer, he seeks to promote the university&#39;s three-part mission of education, research and service in innovative ways. He is working to fill two vice chancellor positions &#8211; vice chancellor of legal affairs and administrative vice chancellor. </p>
<p>Block was previously the vice president and provost at the University of Virginia. As a neuroscientist, Block is researching the effects of aging on the nervous system and how it impacts biological timing. Block holds faculty appointments in psychiatry and biobehavioral sciences at the medical school, and in physiological science at the College of Letters and Science.</p>
