      <div class="small-9 columns">
        <h2>Gene Block</h2>
        <p>Gene Block is the chancellor of UCLA. As chief executive officer, he oversees the university&#39;s three-part mission of education, research and service. Block&#39;s goal is to find innovative ways of promoting UCLA&#39;s mission. He is working to fill two vice chancellor positions, vice chancellor of Legal Affairs and administrative vice chancellor.</p>
        <p>Chancellor Block was previously the vice president and provost of the University of Virginia. As a neuroscientist, Block is researching the effects of aging in the nervous system and how it impacts biological timing. Block holds faculty appointments in psychiatry and biobehavioral sciences in the school of medicine, and in physiological science in the College of Letters and Science.</p>
      </div>
    </div>

