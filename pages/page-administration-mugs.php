        <h2>John Mazziotta</h2>
        <p>Dr. John Mazziotta is the vice chancellor of UCLA Health Sciences and chief executive officer of UCLA Health. This year, Mazziotta&#39;s office aims to integrate education, research and patient care within the David Geffen School of Medicine at UCLA, UCLA Health and other health sciences schools. To maintain the high rankings of UCLA&#39;s medical center, Mazziotta said he wants to increase philanthropic support and move forward in scientific research across the different health schools. </p>
<p>Mazziotta was previously the chair of the department of neurology. He established the Brain Mapping Center at UCLA and was the principal investigator of the International Consortium for Brain Mapping, a group that aims to create the first atlas of the human brain by collecting data from thousands of human subjects. From 2012 to 2015, he was associate vice chancellor for Health Sciences and executive vice dean of the David Geffen School of Medicine.</p>
      </div>
