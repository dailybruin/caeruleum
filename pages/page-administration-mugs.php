      <div class="small-3 columns">
        <img src="http://dailybruin.com/images/2015/09/2.jpg"/>
      </div>
      <div class="small-9 columns">
        <h2>Janina Montero</h2>
        <p>Janina Montero is the vice chancellor of Student Affairs. Student Affairs aims to support students&#39; need by providing services that support academic development and success. </p>
        <p>Montero said she aims to improve the campus climate by providing students opportunities to explore difficult issues related to identity and culture. Montero&#39;s office will collaborate with Jerry Kang, vice chancellor for Equity, Diversity and Inclusion, to assess the program&#39;s effectiveness. </p>
        <p>Montero&#39;s first program this year will allow new first-years and transfer students to share their diverse identities in an event called &#34;Rehearsal for Bruin Life&#34; on Sept. 21. Montero&#39;s office also wants to find ways to provide more space for student organizations and provide programs that help students prepare for careers.</p>
        <p>Montero was the dean of Student Life at Princeton University in 1993 and the vice president of Campus Life and Student Services in 2000. </p>
      </div>
    </div>

    <div class="person" id="jerry-kang">
      <div class="small-3 columns">
