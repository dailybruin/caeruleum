        <h2>Steven A. Olsen</h2>
        <p>Steven A. Olsen is the acting administrative vice chancellor, as well as vice chancellor and chief financial officer. Olsen is responsible for the budget and strategic planning, as well as managing the planning and design of campus buildings and overseeing UCLA&#39;s investment policies, including endowments.</p>
<p>Olsen graduated from UCLA in 1977 and holds a master&#39;s degree from UC Berkeley. He previously worked in the Legislative Analyst&#39;s Office and in the California Department of Finance. Olsen is also a member on the board of directors at the Geffen Playhouse, and a classically trained cellist who performs in Southern California musical communities.</p>
