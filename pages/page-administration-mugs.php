        <h2>Carole Goldberg</h2>
        <p>Carole Goldberg is the vice chancellor of academic personnel. She said the UCLA Academic Personnel Office&#39;s priority is to support UCLA recruitment, retention and recognition of teachers and scholars.</p>
<p>This year, Goldberg intends to implement a new online system for staff to contact each other, conduct a faculty salary equity report and support faculty who want to remain engaged with UCLA after retirement.</p>
<p>Outside of UCLA, Goldberg serves as a justice of the Hualapai Tribe Court of Appeals in Arizona. Between 2011 and 2014, Goldberg served on the Indian Law and Order Commission as appointed by President Barack Obama.</p>
