        <h2>Janina Montero</h2>
        <p>Janina Montero is the vice chancellor of student affairs. UCLA Student Affairs aims to support students&#39; needs by providing services that support academic development and success. </p>
<p>Montero said she aims to improve campus climate by providing students opportunities to explore difficult issues related to identity and culture. </p>
<p>Montero&#39;s first program this year will allow new first-year and transfer students to share their diverse identities in an event called &#34;Rehearsal for Bruin Life&#34; on Sept. 21. </p>
<p>Montero was the dean of student life at Princeton University from 1993 to 2000 and the vice president of campus life and student services at Brown University from 2000 to 2003.</p>
