        <h2>Steven A. Olsen</h2>
        <p>Steve Olson is vice chancellor and chief financial officer for UCLA. Olson is responsible for the budget and strategic planning, as well as managing the planning and design of campus buildings and overseeing UCLA&#39;s investment policies.</p>
        <p>Olson graduated from UCLA in 1977 and holds a master&#39;s degree from UC Berkeley. He previously worked in the Legislative Analyst&#39;s Office and in the California Department of Finance. Olsen is also a member of the Board of Directors of the Geffen Playhouse, and a classically trained cellist who performs in Southern California musical communities.</p>
