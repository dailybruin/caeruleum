        <p>Kevin Reed is the vice chancellor of legal affairs, a position which primarily provides campuswide legal advice to administrators. In the over seven years Reed has served at UCLA, Reed defended against several prominent charges filed against the university. The University of Oregon hired Reed to serve as the chief legal adviser under the title of vice president and general counsel. He will begin his new job at Oregon on Oct. 5. </p>
