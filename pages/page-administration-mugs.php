      <div class="small-9 columns">
        <h2>Carol Goldberg</h2>
        <p>Carol Goldberg is the vice chancellor of Academic Personnel. She said Academic Personnel&#39;s priority is to support UCLA recruitment, retention and recognition of world-class teachers and scholars.</p>
        <p>This year, Goldberg intends to implement a new electronic faculty information system, conduct a faculty salary equity report and support faculty who want to remain engaged with UCLA after retirement.</p>
        <p>Outside of UCLA, Goldberg has served as Justice of the Hualapai Court of Appeals in Arizona. Between 2011 and 2014, Goldberg served on the Indian Law and Order Commission as appointed by President Barack Obama. </p>
