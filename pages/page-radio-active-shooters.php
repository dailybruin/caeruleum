			<h3>Office of Residential Life prepares for active shooters in the dorms</h3>

			<h4>By Joseph Palomo</h4>

			<div class="audio">
				<iframe width="100%" height="450" scrolling="no" frameborder="no" src="https://w.soundcloud.com/player/?url=https%3A//api.soundcloud.com/tracks/249920353&amp;auto_play=false&amp;hide_related=false&amp;show_comments=true&amp;show_user=true&amp;show_reposts=false&amp;visual=true"></iframe>
			</div>

			<p><b>Palomo:</b> Imagine it's a bright, sunny day just like any other here at UCLA. You're sitting in your dorm room, studying for midterms, when you hear loud gunshots. And then you hear screams. What would you do? </p>

			<p>With the recent shooting in San Bernardino on Dec. 2 as well as the shooting at UC Santa Barbara on May 23, 2014; many must wonder just how safe they are living on campus. Rodrigo Aranda is a second-year biology student living in Sproul Hall, and he sees the potential threat of an active shooter.</p>

			<p><b>Aranda:</b> I feel that there's going to be a mass shooting here within the next decade. </p>

			<p><b>Palomo:</b> With approximately ten thousand students residing in campus dorms, maintaining safety and peace of mind is crucial to the Hill's stability. Raymond Lu is a fourth-year political science student. He is also in his second year as a resident assistant. Like every other RA, he has gone through active-shooter training.</p>

			<p><b>Lu:</b> Active-shooter training is when university police and other various departments throughout our campus come to talk to us about safety protocol, safety tips and what we should do in situations of stress and situations of violence. If anyone brings a gun, how do we respond? What's our first priority? What do we do to keep ourselves safe? And how do we help others maintain that safety? <p>

			<div class="image">
				<img src="http://dailybruin.com/images/2016/03/reslife.jpg">
				<span class="caption">Daily Bruin file photo</span>
			</div>

			<p><b>Palomo:</b> For some students, the current active-shooter protocol and training is simply not enough. They believe armed security should be considered.</p>

			<p><b>Aranda:</b> I feel that it would make campuses safer for the most part because if something were to go down, there would be somebody, or multiple people, on campus all ready to, you know, react to whatever is going on.</p>

			<p><b>Palomo:</b> The Office of Residential Life works closely with the UCPD to ensure the safety of students. If an RA needs an officer or a member of security to help handle a situation, they can have full confidence that they will not be left alone.</p>

			<p><b>Lu:</b> We as ResLife -- this is my opinion -- we as ResLife have a good relationship with UCPD because they help us enforce laws that we can't because we essentially as RA's don't have much power to actually tell someone, "You have to leave our campus," or escort someone off our campus. So we depend on UCPD for that who actually has the power of enforcement. </p>

			<p><b>Palomo:</b> So there is active-shooter training for RAs, and they have support from the UCPD. RAs and security have various duties such as patrols to ensure that a violent act will not occur. On top of that, UCLA is located in Westwood, a neighborhood for which the <a href="http://maps.latimes.com/neighborhoods/neighborhood/westwood/crime/">LA Times reports a low rate of violent crimes</a>. </p>

			<p><b>Lu:</b> I don't speak for ResLife, but for me personally, I've been here for four years and ... I believe our community, the Hill, is very safe with all the access controls and patrols we have. But it's not a pertinent thing in our minds, but it could happen. </p>

			<p><b>Palomo:</b> Steven Lee, a third-year transfer student who is majoring in political science and lives in the university apartments, believes that UCLA has a "laid-back" vibe that should not be ruined with fear and excessive preventative measures for a threat that may never happen.</p>

			<p><b>Lee:</b> No, I think UCLA's carefree attitude is something that should be admired and praised and something that shouldn't be changed. Knock on wood, I hope nothing happens, but if something does happen, I hope that it doesn't scare everybody into living in fear.</p>

			<p><b>Palomo:</b> To some, this sense of security is simply naive. Aranda feels that the sheltered backgrounds many UCLA students come from blind them from the real threat of an active shooter. He says UCLA already takes steps to bring awareness to subjects such as sexual assault and alcohol abuse through educational videos and courses, so perhaps it is time to bring gun violence in university housing into the mix as a preventative measure.</p>

			<p><b>Aranda:</b> I feel that a lot of people are naive and they wouldn't know what to do if something were to happen. And I think those videos, although they're not a lot, they're better than nothing. To give a general idea of what one should do if something were to happen.</p>

			<p><b>Palomo:</b> It's hard to say whether or not UCLA is doing enough to prevent the threat of an active shooter in university housing. RA training is based primarily on instinct because of the spontaneous nature of a shooting.</p>

			<p><b>Lu:</b> One of the things training specified is there is not one absolute way to handle if there is an active shooter on campus. There's no one way to deal with it -- you have to assess the situation to the best of your own ability and act upon your own instincts. If an active shooter comes, what we say is: We need to evaluate our environment and see where the closest exits are, what we could use to barricade doors, what we could use to protect ourselves, what we could use as weapons, what happens if the cop comes, what do we do as leaders of the community to keep ourselves safe and others safe.</p>

			<p><b>Palomo:</b> It is fair to say that most UCLA students do not anticipate a shooting in areas of residence. It has never happened here before, and hopefully it never will. However, the lack of anticipation does not mean that the university isn't taking precautions to prevent one. It has empowered RAs and the UCPD to act in whatever way necessary. The real question seems to be whether or not students need to begin a conversation regarding their own safety or if the status quo will do for now.</p>

			<p>For Daily Bruin Radio, this is Joseph Palomo.</p>







