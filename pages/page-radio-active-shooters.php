			<h3>Conflict persists about constitutionality of on-campus gun control</h3>

			<h4>By Marisa Piceno</h4>

			<div class="audio">
				<iframe width="100%" height="450" scrolling="no" frameborder="no" src="https://w.soundcloud.com/player/?url=https%3A//api.soundcloud.com/tracks/249923370&amp;auto_play=false&amp;hide_related=false&amp;show_comments=true&amp;show_user=true&amp;show_reposts=false&amp;visual=true"></iframe>
			</div>

			<p><b>Piceno:</b> Lately, in the wake of shootings on college campuses, a new question has been introduced as to whether students should have the right to carry a gun on school grounds. These shootings at schools have left many students scared, helpless and defenseless to come to class, raising the possibility of stricter on-campus gun control.</p>

			<p><b>Winkler:</b> Government has good reason to limit guns in certain places, and if government believes that colleges are places that are is too sensitive, then it has a good reason to support that. Unlikely to be unconstitutional, don&rsquo;t necessarily think it&rsquo;s the best policy, but it would survive.</p>

			<p><b>Piceno:</b> Adam Winkler is a professor at UCLA school of law and is the author of Gunfight: The Battle over the Right to Bear Arms in America. He recognizes that Americans have always had gun rights, and at the same time we&rsquo;ve also had gun control. And those two ideas are not compatible.</p>

			<p>At UCLA, this controversy has created a divide between students over whether or not legislation allowing concealed weapons on college campuses should be passed. Supporters of pro-gun movements believe that students have every right to protect themselves from potential attackers. On the opposing side, gun control advocates think schools should tighten gun policies to keep guns off campus.</p>

			<div class="image">
				<img src="http://dailybruin.com/images/2016/03/schooloflaw.jpg">
				<span class="caption">Zhitong Qian/Daily Bruin</span>
			</div>

			<p><b>Hordequin:</b> I do think that there needs to be a mandatory no-gun policy at universities in light of these different shootings on college campuses and students feeling they aren&rsquo;t safe. It&rsquo;s just one more step in ensuring that students are able to go to class, get their education in a way that they are comfortable.</p>

			<p><b>Piceno:</b> Current undergraduate students association council internal vice president Heather Hourdequin believes that the lack of guns on college campuses keeps students safe. She says safety is her top priority.</p>

			<p><b>Hordequin:</b> I&rsquo;ve been programming events to increase interactions between students and university police in a way that isn&rsquo;t confrontational. Often times students' first interactions with UCPD are coming to somebody&rsquo;s apartment, busting them for noise or pulling them over for speeding or violating a traffic stop.</p>

			<p><b>Piceno:</b> Rather than allowing students to carry armed weapons, Hourdequin believes there are other ways to keep college campuses safe. She has also worked with the West Side Alliance, UCPD, landlords and other campus entities that target safety initiatives at UCLA.</p>

			<p><b>Hordequin:</b> I really want to have students feel comfortable going to UCPD, and so we&rsquo;ve been having open forums ever since I&rsquo;ve been internal vice president. We&rsquo;ve had pizza with police, and I hope to program an event next quarter in which students can engage and interact with UCPD in a more humanizing way.</p>

			<p><b>Piceno:</b> On the opposing side, supporters of pro-gun movements believe that students have every right to protect themselves from potential attackers. These tragedies indicate a need to dismiss the current gun regulations and instead allow concealed weapons on college campuses.</p>

			<p>Current Bruin Republicans president and fourth-year economics and political science student Jake Kohlhepp supports concealed carry and dismisses gun-free zones on college campuses.</p>

			<p><b>Kohlhepp:</b> I&rsquo;m actually someone that supports allowing concealed carry and firearms on campus. As long as people lawfully obtain those firearms, I think that should be fine. I tend to feel that shooters are more likely to target places that are already declared as gun-free zones, specifically because the people that follow those types of rules are what we would term the &quot;good guys,&quot; the people that would be fighting for justice.</p>

			<p><b>Piceno:</b> Kohlhepp believes that if you have a mass shooter and a group of unarmed students, it is very difficult for a group of students to stop the event because it involves at least several people choosing to sacrifice their lives. Rather than enforcing stricter gun control regulations, Kohlhepp is confident that if concealed gun carry on college campuses were allowed, students can protect other students if a mass shooting were to ever occur on a college campus.</p>

			<p><b>Kohlhepp:</b> When you have someone with a gun, as opposed to someone without a gun, the odds change. Everything is completely different, because now you can actually stop that person without having to put that person&rsquo;s life completely in danger.</p>

			<p><b>Piceno:</b> According to armedcampuses.org, the current majority of colleges and universities in the United States, such as Wyoming, New Mexico, New York and many more, prohibit firearms on their campuses. As of right now, measures are currently being taken by higher education committees across the United States to allow concealed guns on college campuses. Schools in Arkansas, Colorado, Idaho, Kansas, Mississippi, Oregon, Utah, Texas and Wisconsin now allow firearms on their premises, and there are similar measures pending in other states. At UCLA, the possession of firearms and other weapons are prohibited on the UCLA campus, off-campus buildings owned or operated by the University and areas adjacent to university property.</p>

			<p>With this divide between students, this constant debate will only continue to proliferate and students will continue to voice their judgement on gun control at UCLA and beyond. </p>

			<p>For Daily Bruin Radio, this is Marisa Piceno.</p>







