			<h3>Office of Emergency Management prepares for active shooter situation</h3>

			<h4>By Nikki Harris</h4>

			<div class="audio">
				<iframe width="100%" height="450" scrolling="no" frameborder="no" src="https://w.soundcloud.com/player/?url=https%3A//api.soundcloud.com/tracks/249920519&amp;auto_play=false&amp;hide_related=false&amp;show_comments=true&amp;show_user=true&amp;show_reposts=false&amp;visual=true"></iframe>
			</div>

			<p><b>Harris:</b> Santa Monica College in 2013. UC Santa Barbara in 2014. Umpqua Community College in 2015. These three schools reflect one national trend: active shootings have <a href="https://www.fbi.gov/about-us/office-of-partner-engagement/active-shooter-incidents/a-study-of-active-shooter-incidents-in-the-u.s.-2000-2013">more than doubled in number since 2007</a>, the year a fourth-year English student at Virginia Tech shot 32 people dead and injured 17 more on university grounds. There have been more than <a href="http://everytownresearch.org/school-shootings/">60 shootings</a> on American college campuses since 2010 that have resulted in more than 40 deaths. Many universities across the nation, including UCLA, have restructured their emergency procedure to face this new reality. Art Kirkland, director of UCLA's Office of Emergency Management, explains how UCLA has been especially affected by this trend.</p>

			<p><b>Kirkland:</b> Awareness has gotten a lot better. I think folks, you know, before 2007 this was not a subject that would have ever come up. Nobody would have thought about this. And every time one happens, even though they are exceedingly rare, they are much more rare than lightning strikes -- but they are also, they're gonna happen, they're gonna happen somewhere, just like lightning strikes -- and I think the level of awareness has gone up since UCSB because now we've got, it's close to home. The UC, even though we're all over the state, we're still one big family and that affected our family.</p>

			<div class="image">
				<img src="http://dailybruin.com/images/2016/03/jansssteps.jpg">
				<span class="caption">Laura Uzes/Daily Bruin</span>
			</div>

			<p><b>Harris:</b> With a population of 68,000 students, staff and faculty and a campus of 419 acres, UCLA's size alone impedes the ability of the administration to fully prepare for an active shooter. When emergencies strike such a large school, time is of the essence.</p>

			<p><b>Kirkland:</b> Active shooter events on college campuses are typically over before you even know they're going on. Northern Illinois University is a good example: From the time the shooter appeared and people knew something was wrong until the time the incident was over was one minute and 18 seconds. In Birmingham, that one was over in 30 seconds. So there's not a lot of time to react.</p>

			<p><b>Harris:</b> The Office of Emergency Management, or OEM, combats this timing problem through BruinAlert, a campus-wide alert system that informs students of threats on or near the UCLA campus. BruinAlert uses several different technologies to reach students, including text message, an email and a post on Twitter and Facebook. They override AM 1630, UCLA's 24/7 campus broadcast, and every cable television channel on campus. In a shooting, they would also air external sirens located across campus. With a campus this large, information can save lives.<p>

			<p><b>Kirkland:</b> If there were a shooting in Murphy Hall, you need to know to stay away from Murphy Hall. If you're in Murphy Hall, you need to know to lock the doors, run away if you can ... and then not do anything unless you're told to. The last thing the police needs while they're trying to run down an active shooter is everybody in Murphy Hall running out to hug the police for coming to their rescue.</p>

			<p><b>Harris:</b> Some students have acknowledged disappointment with BruinAlert's consistency, saying it arbitrarily covers some incidents and ignores others. But Kirkland explains that police have to verify a threat for a BruinAlert to be issued, a process that can outlast the initial threat.</p>

			<p><b>Kirkland:</b> We have different levels of validation. Active shooters are one of those things that have a very low validation level. If I've got any reason to believe there is an active shooter then an alert will go out.</p>

			<p><b>Harris:</b> UCLA has procedure in place but does not require faculty, staff or students to undergo active shooter training. The administration focuses on how the university would react after the event but leaves it up to individual students and departments to seek out safety training from UCPD. Alicia Espinosa, the facilities and auxiliary services manager for UCLA's psychology department, coordinated an active shooter training in September. She believes the trainings helps faculty and staff recognize potential threats.</p>

			<p><b>Espinosa:</b> I think they're very valuable. It brings into discussion -- people will discuss it more and be more mindful, not just ignoring people coming in and out. You put it on your radar and notice. That's what these trainings actually provide, make you more alert. It kind of tells you what to watch out for -- now you're like, I'm going to report it.</p>

			<p><b>Harris:</b> Kirkland said requiring training for faculty and students would be worthwhile, but the sheer number of students makes training for them a logistical feat. Kirkland added seminars are most effective in groups of 25 to 30 students, but dividing a student population of 40,000 like that is a near-impossible task to coordinate in time and instruction. That's not to say nobody is trained -- UCLA's annual safety report says that in 2014, "UCPD crime prevention presentations and awareness programs reached approximately 24,000 individuals." Although staff and faculty are strongly urged to attend these events, the emotional nature of their content prevents UCLA administration from requiring attendance.</p>

			<p><b>Kirkland:</b> Even the training bothers some people. They're upset by having to deal with the concept. So they're not going to require people to sit through training that could be psychologically. Folks have been through trauma in the past and this might make them relive the trauma, and we're really cognizant of that, and that's one of the reasons why I believe we've never said it's a requirement for everyone to go through training.</p>

			<p><b>Harris:</b> However, many students would feel safer knowing that both they and the rest of UCLA's campus had a basic understanding of emergency response. Sheridan Bowers, a third-year international development studies student, was visiting her sister in Isla Vista when the UC Santa Barbara shooting occurred in 2014. She believes that training provides a important framework for surviving an active shooter situation.</p>

			<p><b>Bowers:</b> I think if students at least have the training and the background so that it's at least in their subconscious, even if they're not thinking about it in the moment, it still has been sitting in their subconscious for a week, a month, a year.</p>

			<p><b>Harris:</b> UCLA Anderson School of Management held a safety training in December titled "Shots Fired: When Lightning Strikes." Prompted by the recent tragedy in Paris, the safety seminar is one example of UCLA preparing itself for disaster. This kind of training can be invaluable should lightning ever strike at UCLA, a rarity that, these days, doesn't seem so unlikely after all.</p>

			<p>For Daily Bruin Radio, this is Nikki Harris. </p>





