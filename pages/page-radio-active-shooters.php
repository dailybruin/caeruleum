			<h3>UCPD prepares for active shooter situations</h3>

			<h4>By Amanda Rubalcava</h4>

			<div class="audio">
				<iframe width="100%" height="450" scrolling="no" frameborder="no" src="https://w.soundcloud.com/player/?url=https%3A//api.soundcloud.com/tracks/249920938&amp;auto_play=false&amp;hide_related=false&amp;show_comments=true&amp;show_user=true&amp;show_reposts=false&amp;visual=true"></iframe>
			</div>

			<p><b>Rubalcava:</b> In a room full of staff and students beneath Pauley Pavilion, loud gunshots were heard blasting from upstairs. Some shrieked, some ran for cover, but most remained seated. Fortunately on this Friday afternoon, every shot was a blank.</p>

			<p><b>Littlestone:</b> We have done training like this and drills for different departments on campus and presented something similar to this to different student groups, staff groups and faculty groups on campus in the past as well.</p>

			<p><b>Rubalcava:</b> That was Lt. Mark Littlestone of the UCLA Police Department. He was one of the speakers at an active shooter response training workshop here at UCLA.</p>

			<p><b>Littlestone:</b> So, UCLA is a big target. We're worldwide known, so should a terrorist decide that they wanted to make a big name for themselves, UCLA might be something that they're looking at. However, with the kinds of people that we have on this campus, and the collegiality and the fact that people work together to try to report on things where they see that someone needs assistance, that's not a place they're going to want to think about.<p>

			<div class="image">
				<img src="http://dailybruin.com/images/2016/03/ucpd.jpg">
				<span class="caption">Jessica Zhou/Daily Bruin</span>
			</div>

			<p><b>Rubalcava:</b> The gunshots in Pauley were harmless, but they weren't last December in the deadly San Bernardino shooting. Same for the shots which echoed through the streets of Isla Vista two years back. As active shooter situations like these seem to become an unpredictable and deadly reality, UCPD has been educating officers for possible attacks. Luis De Vivero, a sergeant in the Threat Management Unit for UCPD, acknowledges an evolution of active shooter trainings that helps police respond to these high-stakes threats.</p>

			<p><b>De Vivero:</b> Active shooter training has changed over the years. So it's no longer waiting for three or four officers to arrive to a particular location, setting up, then going into the area to respond to. Active shooter training has now evolved to where the first person responding to the location is going to engage and take an active role in stopping the threat.</p>

			<p><b>Rubalcava:</b> Being prepared for an active shooter situation is becoming a standardized component of police training. With the incorporation of tactical training following historical shootings, response preparation is learning from the past to help survival in the future.</p>

			<p><b>De Vivero:</b> We look at all cases, not just local ones that happened on campus ... We constantly look at how to improve ourselves, so we look at all incidents and try to figure out what we can do to prevent them because obviously the threats are always changing. They're dynamic as well.</p>

			<p><b>Rubalcava:</b> Police preparedness consists of more than purely in-field training. Beyond the officers that roam campus any given day, Littlestone states that having other forces join in and help in the case of an attack develops a more efficient response.</p>

			<p><b>Littlestone:</b> Plus we have mutual aid agreements with other agencies around us like the Los Angeles Police Department, Culver City and Santa Monica, and our federal partners across the street on Wilshire are also going to respond to something like this. I mean it's going to be a county-wide response should we have an active shooter incident at UCLA.</p>

			<p><b>Rubalcava:</b> So while the police are taking past cases and learning from them, what should the students be doing? After all, out of the 160 active shooter incidents between 2000 and 2013, 24.4% have happened in educational environments, according to FBI.</p>

			<p><b>Littlestone:</b> One of the things that we stress here that is important to recognize is that safety is everyone's responsibility. They can't always rely on having a police officer or some other first responder right around the corner should something like this happen.</p>

			<p><b>Rubalcava:</b> When an active shooter situation happened at Santa Monica College back in 2013, the library was one place the gunman targeted. I asked Littlestone to guide us through what a Bruin should do if the same thing were to happen here at Powell Library.</p>

			<p><b>Littlestone:</b> Again, it's gonna depend on what they hear and know at the time. Should they hear shots, then the first response that we tell people is to try to get out of the building. That's the "Run" option should they be notified.</p>

			<p><b>Rubalcava:</b> The "Run" Option that Littlestone mentions is the first option in the "Run. Hide. Fight." motto that the FBI promotes in order to survive an active shooter event.</p>

			<p><b>Littlestone:</b> Should it be too close to them when they hear the shots outside their study area, then we're gonna suggest that they hide and start to prepare for what to do should that shooter breach the area that they're in. Then they've got to prepare to fight.</p>

			<p><b>Rubalcava:</b> But would UCLA students be prepared if this were to happen to our campus?</p>

			<p><b>De Vivero:</b> I wouldn't use those exact words. I think the UCLA students can be prepared, I just think there are areas that we can work to improve. The students are the least-reached group of folks we have here on campus. I would say the staff have more resources and have attended more active shooter presentations.</p>

			<p><b>Rubalcava:</b> UCPD hopes to organize a presentation for all student groups on campus, similar to the presentations shown to incoming freshman or transfer students. For now, student research and awareness are key to better safety. If fatal gunshots are ever fired on campus, as opposed to the blanks heard at Pauley Pavilion, everyone's survival depends on the steps they've taken to prepare.</p>

			<p>For Daily Bruin Radio, this is Amanda Rubalcava.</p>







